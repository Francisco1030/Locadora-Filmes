    private void cadastrar() {
        try {
            cliente.setNome(txt_nome_cliente.getText());
            cliente.setCpf(txt_cpf.getText());
            cliente.getEndereco().setRua(txt_rua.getText());
            cliente.getEndereco().setNumero(txt_n.getText());
            cliente.getEndereco().setCidade(txt_cidade.getText());
            cliente.setTelefone(txt_telefone.getText());

            dao.inserir(cliente);
            Mensagens.mensagemConfirmacao("Cliente cadastrado com sucesso!");
        } catch (Exception e) {
            Mensagens.mensagemErro(e.getMessage());
        }
        preecheTabela();
        limpaCampos();

    }

    private void atualizar() {
        try {
            btn_cadastrar.setEnabled(true);
            cliente.setNome(txt_nome_cliente.getText());
            cliente.setCpf(txt_cpf.getText());
            cliente.getEndereco().setRua(txt_rua.getText());
            cliente.getEndereco().setNumero(txt_n.getText());
            cliente.getEndereco().setCidade(txt_cidade.getText());
            cliente.setTelefone(txt_telefone.getText());
            cliente.setId(Integer.parseInt(lbl_id.getText()));

            dao.editar(cliente);
            Mensagens.mensagemConfirmacao("Cliente atualizado com sucesso!");
        } catch (Exception e) {
            Mensagens.mensagemErro(e.getMessage());
        }
        preecheTabela();
        limpaCampos();

    }

    private void apagar() {
        try {
            btn_cadastrar.setEnabled(true);
            cliente.setNome(txt_nome_cliente.getText());
            cliente.setCpf(txt_cpf.getText());
            cliente.getEndereco().setRua(txt_rua.getText());
            cliente.getEndereco().setNumero(txt_n.getText());
            cliente.getEndereco().setCidade(txt_cidade.getText());
            cliente.setTelefone(txt_telefone.getText());
            cliente.setId(Integer.parseInt(lbl_id.getText()));

            dao.excluir(cliente);
            Mensagens.mensagemConfirmacao("Cliente deletado com sucesso!");
        } catch (Exception e) {
            Mensagens.mensagemErro(e.getMessage());
        }
        preecheTabela();
        limpaCampos();

    }

    private void preecheTabela() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) table_cliente.getModel();
            modelo.setNumRows(0);
            dao = new ClienteDao();
            for (Cliente clientes : dao.listar()) {
                modelo.addRow(new Object[]{clientes.getId(),
                    clientes.getNome(),
                    clientes.getCpf(),
                    clientes.getEndereco().getRua(),
                    clientes.getEndereco().getNumero(),
                    clientes.getEndereco().getCidade(),
                    clientes.getTelefone()});
            }
        } catch (Exception e) {
            Mensagens.mensagemErro(e.getMessage());
        }
    }

    private void limpaCampos() {
        txt_nome_cliente.setText("");
        txt_cpf.setText("");
        txt_rua.setText("");
        txt_n.setText("");
        txt_cidade.setText("");
        txt_telefone.setText("");
        lbl_id.setText("");
    }

